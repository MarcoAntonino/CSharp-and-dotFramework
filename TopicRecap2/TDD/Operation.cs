using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace TDD
{
    class Operation
    {
        internal int Sum(int numberOne, int numberTwo)
        {
            return numberOne + numberTwo;
        }

        internal int Dif(int numberOne, int numberTwo)
        {
            return numberOne - numberTwo;
        }
    }
}
